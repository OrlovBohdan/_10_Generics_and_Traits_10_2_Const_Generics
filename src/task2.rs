fn main() {
    let arr = [1, 2, 3];
    print_array(&arr); // Передаем ссылку на массив

    let arr = ["hello", "world"];
    print_array(&arr); // Передаем ссылку на массив
}
// Заполняем пропуски для универсальной функции
fn print_array<T: std::fmt::Debug>(arr: &[T]) {
    println!("{:?}", arr);
}

/*
Обобщение по типу: Добавлено параметр типа T, который ограничен трейтами Debug.
Это позволяет использовать любые типы, которые могут быть выведены на экран с помощью println!.
Ссылка на массив: Мы изменили тип аргумента на ссылку &[T], чтобы передать массив в функцию.
Это позволяет избежать копирования и поддерживать разные размеры массивов.
*/